    <p>Daniel Trezel débute en 1639 la culture de canne à sucre en Martinique. Il construit son premier moulin à Saint-Pierre, moulin qui abrite désormais la distillerie Barburantho et “qui est le symbole emblématique du rhum Barburantho”.</p>

    <figure>
      <img src="" alt="" />
      <figcaption></figcaption>
    </figure>

    <p>En 1667 Père Du Tertre fabrique un appareil de distillation pour traiter l’écume et le sirop, et créé la mélasse, une eau de vie associée à du jus de canne à sucre.</p>
    <p>C’est à ce moment du XVII siècle que les termes guildive et tafia viennent remplacer le terme d’eau de vie de mélasse pour enfin être remplacé par le terme rhum originaire des Antilles britanniques.</p>
    <p>A la fin du XVII siècle la production du rhum est perfectionné par le Père Labat avec la l’élaboration d’un alambic ce qui lança la création de nombreuses distilleries Martiniquaises.</p>
    
    <figure>
      <img src="" alt="" />
      <figcaption></figcaption>
    </figure>

    <p>C’est suite à ces avancés et cette culture du rhum martiniquais qu’en 1706 née la distillerie Barburantho. Tout d’abord comme une distillerie artisanale et familiale, sa renommée la fait évoluer au niveau de l’île puis de la France, et maintenant la qualité de son rhum et son <a href="../histoire.php">savoir-faire vieux de plus de 300 ans</a> en fait une distillerie emblématique pour les amateurs et professionnels de rhum à travers le monde.</p>
